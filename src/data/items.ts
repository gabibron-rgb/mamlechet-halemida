export type ItemCategory =
  | 'furniture' | 'decoration' | 'storage' | 'upgrade'
  | 'companionItem' | 'trophy' | 'cosmetic';

export type Zone = 'wall' | 'floor' | 'shelf' | 'desk' | 'special' | 'petarea';


export type Item = {
  id: string;
  nameHe: string;
  descriptionHe: string;
  category: ItemCategory;
  theme: ThemeId;
  rarity: Rarity;
  zones: Zone[];
  size: 1 | 2 | 3;
  price: number;       // shop price (0 if not shop-sold)
  sellValue: number;   // ~50% of price; 0 if bound
  levelRequired: number;
  isBound: boolean;
  isShelfOnly: boolean;
  source: 'shop' | 'box' | 'levelReward' | 'teacherTrophy' | 'classUnlock';
  modelRef: string;    // primitive hint: 'cube' | 'cylinder' | 'sphere' | 'cone' | 'torus'
  displayKind?: DisplayKind;
  xpOnPurchaseBonus?: number;
  caption?: string;    // engraved text on trophies
};

